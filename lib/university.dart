class University {
  final String photo;
  final String name;
  final String desc;
  final String city;
  final String phoneNumber;
  final String universityNumber;
  final List<String> professions;

  University({
    required this.photo,
    required this.name,
    required this.desc,
    required this.city,
    required this.phoneNumber,
    required this.universityNumber,
    required this.professions,
  });
}
